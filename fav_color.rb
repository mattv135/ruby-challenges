fav_color = "pink"

case fav_color
when 'red'
	puts "Red like fire!"
when 'orange'
	puts "Orange, like, well... an orange!"
when 'yellow'
	puts "Yellow daffodils are so pretty in the Sping!"
when 'green'
	puts "Have you been to the Emerald City in Oz?"
when 'blue'
	puts "Blue like the sky!"
when 'purple'
	puts "Like purple drank, yo!"
else
	puts "Hm, well I don't really know what color that is!"
end

