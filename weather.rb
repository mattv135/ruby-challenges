weather = "sunny"

case weather
when 'sunny'
	puts "Get out the sunscreen and enjoy your day!"
when 'cloudy'
	puts "Bring a light sweater"
when 'foggy'
	puts "Careful on the road!"
when 'rainy'
	puts "Make sure you have an umbrella!"
when 'snowing'
	puts "It's cold outside! Keep warm!"
else
	puts "Be prepared for anything!"
end